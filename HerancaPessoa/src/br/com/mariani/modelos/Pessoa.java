package br.com.mariani.modelos;

/**
 *
 * @author maryucha
 */
public class Pessoa {
    
    private String nome;
    private int idade;
    private char sexo;

    public Pessoa(String nome, int idade, char sexo) {
        this.nome = nome;
        this.idade = idade;
        this.sexo = sexo;
    }

    public Pessoa() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdadae(int idade) {
        this.idade = idade;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }
    
    public void imprime(){
        System.out.println("NOME "+this.nome+" | IDADE "+this.idade+" | SEXO "+this.sexo);
    }
    
}
